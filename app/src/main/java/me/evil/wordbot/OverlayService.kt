    private fun isKnownEnglishWord(word: String): Boolean {
        if (word.length < 3) return false
        return masterDictionarySet.contains(word.uppercase())
    }

    private fun findValidWordsMatchingGridPatterns(grid: List<List<Letter>>): List<String> {
        val numRows = grid.size
        if (numRows == 0) return emptyList()

        val directions = listOf(
            Pair(0, 1), Pair(0, -1), Pair(1, 0), Pair(-1, 0),
            Pair(1, 1), Pair(1, -1), Pair(-1, -1), Pair(-1, 1)
        )

        val dbByLength = masterDictionarySet.groupBy { it.length }
        val matchedWords = mutableSetOf<String>()

        for (r in 0 until numRows) {
            val numCols = grid[r].size
            for (c in 0 until numCols) {
                for ((dr, dc) in directions) {
                    val patternChars = mutableListOf<Char>()
                    var nr = r
                    var nc = c
                    while (nr in 0 until numRows && nc in 0 until grid[nr].size) {
                        val rawCh = grid[nr][nc].char
                        val ch = if (rawCh in 'A'..'Z') rawCh else '?'
                        patternChars.add(ch)
                        
                        val len = patternChars.size
                        if (len in 3..12) {
                            val nonWildcards = patternChars.count { it != '?' }
                            if (nonWildcards >= (if (len <= 4) 1 else 2)) {
                                val candidatesForLen = dbByLength[len]
                                if (candidatesForLen != null) {
                                    for (dbWord in candidatesForLen) {
                                        var matches = true
                                        for (i in 0 until len) {
                                            val pCh = patternChars[i]
                                            val wCh = dbWord[i]
                                            if (pCh != '?' && !charMatches(pCh, wCh)) {
                                                matches = false
                                                break
                                            }
                                        }
                                        if (matches) {
                                            matchedWords.add(dbWord)
                                        }
                                    }
                                }
                            }
                        }
                        nr += dr
                        nc += dc
                    }
                }
            }
        }
        return matchedWords.toList()
    }

    private fun findAllValidGridWords(grid: List<List<Letter>>, filterLength: Int? = null): List<String> {
        val found = mutableSetOf<String>()
        val numRows = grid.size
        if (numRows == 0) return emptyList()

        val directions = listOf(
            Pair(0, 1),   // Right
            Pair(0, -1),  // Left
            Pair(1, 0),   // Down
            Pair(-1, 0),  // Up
            Pair(1, 1),   // Down-Right
            Pair(1, -1),  // Down-Left
            Pair(-1, -1), // Up-Left
            Pair(-1, 1)   // Up-Right
        )

        for (r in 0 until numRows) {
            val numCols = grid[r].size
            for (c in 0 until numCols) {
                for ((dr, dc) in directions) {
                    val sb = StringBuilder()
                    var nr = r
                    var nc = c
                    while (nr in 0 until numRows && nc in 0 until grid[nr].size) {
                        val ch = grid[nr][nc].char
                        if (ch in 'A'..'Z') {
                            sb.append(ch)
                            val str = sb.toString()
                            if (str.length >= 3) {
                                if (filterLength == null || str.length == filterLength) {
                                    if (isKnownEnglishWord(str)) {
                                        found.add(str)
                                    }
                                }
                            }
                        } else {
                            break
                        }
                        nr += dr
                        nc += dc
                    }
                }
            }
        }
        return found.toList()
    }

