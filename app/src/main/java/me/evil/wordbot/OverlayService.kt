    private fun setupDraggableHeader(header: View, params: WindowManager.LayoutParams, root: View) {
        var initialX = 0
        var initialY = 0
        var touchX = 0f
        var touchY = 0f

        header.setOnTouchListener { _, event ->
            val metrics = DisplayMetrics()
            windowManager.defaultDisplay.getRealMetrics(metrics)
            val screenWidth = metrics.widthPixels
            val screenHeight = metrics.heightPixels

            when (event.action) {
                MotionEvent.ACTION_DOWN -> {
                    initialX = params.x
                    initialY = params.y
                    touchX = event.rawX
                    touchY = event.rawY
                    true
                }
                MotionEvent.ACTION_MOVE -> {
                    val viewWidth = root.width.coerceAtLeast(100)
                    val viewHeight = root.height.coerceAtLeast(100)

                    val maxDragX = screenWidth - viewWidth
                    val maxDragY = screenHeight - viewHeight

                    // Clamp to prevent view from going out of bounds or getting stuck
                    params.x = (initialX + (event.rawX - touchX).toInt()).coerceIn(0, maxDragX)
                    params.y = (initialY + (event.rawY - touchY).toInt()).coerceIn(0, maxDragY)
                    windowManager.updateViewLayout(root, params)
                    true
                }
                else -> false
            }
        }
    }

    private fun setupDraggableBubble(bubble: View, params: WindowManager.LayoutParams) {
        var initialX = 0
        var initialY = 0
        var touchX = 0f
        var touchY = 0f
        var isLongPressTriggered = false
        var hasDragged = false

        val longPressRunnable = Runnable {
            isLongPressTriggered = true
            try {
                val vibrator = getSystemService(Context.VIBRATOR_SERVICE) as? android.os.Vibrator
                if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
                    vibrator?.vibrate(android.os.VibrationEffect.createOneShot(50, android.os.VibrationEffect.DEFAULT_AMPLITUDE))
                } else {
                    vibrator?.vibrate(50)
                }
            } catch (e: Exception) {
                Log.w(TAG, "Failed to vibrate: ${e.message}")
            }
            showExpanded()
        }

        bubble.setOnTouchListener { _, event ->
            val metrics = DisplayMetrics()
            windowManager.defaultDisplay.getRealMetrics(metrics)
            val screenWidth = metrics.widthPixels
            val screenHeight = metrics.heightPixels

            when (event.action) {
                MotionEvent.ACTION_DOWN -> {
                    initialX = params.x
                    initialY = params.y
                    touchX = event.rawX
                    touchY = event.rawY
                    isLongPressTriggered = false
                    hasDragged = false
                    mainHandler.postDelayed(longPressRunnable, 500)
                    true
                }
                MotionEvent.ACTION_MOVE -> {
                    val distance = Math.hypot((event.rawX - touchX).toDouble(), (event.rawY - touchY).toDouble())
                    if (distance > 15) {
                        hasDragged = true
                        mainHandler.removeCallbacks(longPressRunnable)
                    }
                    if (!isLongPressTriggered) {
                        val viewWidth = bubble.width.coerceAtLeast(100)
                        val viewHeight = bubble.height.coerceAtLeast(100)

                        val maxDragX = screenWidth - viewWidth
                        val maxDragY = screenHeight - viewHeight

                        params.x = (initialX + (event.rawX - touchX).toInt()).coerceIn(0, maxDragX)
                        params.y = (initialY + (event.rawY - touchY).toInt()).coerceIn(0, maxDragY)
                        windowManager.updateViewLayout(bubble, params)
                    }
                    true
                }
                MotionEvent.ACTION_UP, MotionEvent.ACTION_CANCEL -> {
                    mainHandler.removeCallbacks(longPressRunnable)
                    if (!isLongPressTriggered && !hasDragged) {
                        val distance = Math.hypot((event.rawX - touchX).toDouble(), (event.rawY - touchY).toDouble())
                        if (distance < 15) {
                            startSolvingFlow()
                        }
                    }
                    true
                }
                else -> false
            }
        }
    }

