    private val paintGridContainer = Paint().apply {
        style = Paint.Style.STROKE
        strokeWidth = 4f
        isAntiAlias = true
    }

    override fun onDraw(canvas: Canvas) {
        super.onDraw(canvas)
        
        // Draw word highlights
        for (highlight in wordHighlights) {
            if (highlight.isSecret) {
                paintBg.color = 0x33FF4444.toInt() // semi-transparent red for secrets
                canvas.drawRect(highlight.rect, paintBg)
                paintRect.color = 0xFFFF4444.toInt() // red border
                canvas.drawRect(highlight.rect, paintRect)
                
                // Draw text label "? (5)" above the box
                val text = "? (${highlight.wordLength})"
                canvas.drawText(text, highlight.rect.left.toFloat(), highlight.rect.top.toFloat() - 6f, paintLabelText)
            } else {
                paintRect.color = highlight.color
                canvas.drawRect(highlight.rect, paintRect)
            }
        }

        // Draw letter indicators
        for (letter in letters) {
            canvas.drawCircle(letter.x, letter.y, 35f, paintCircle)
            canvas.drawCircle(letter.x, letter.y, 35f, paintBorder)
            
            val textHeight = paintText.descent() - paintText.ascent()
            val textOffset = textHeight / 2 - paintText.descent()
            canvas.drawText(letter.char.toString(), letter.x, letter.y + textOffset, paintText)
        }

        // Draw Topic Detection Box & Level Type Badge
        topicResult?.let { topic ->
            val type = topic.levelType
            paintTopicBox.color = type.color
            canvas.drawRoundRect(
                topic.rect.left.toFloat(),
                topic.rect.top.toFloat(),
                topic.rect.right.toFloat(),
                topic.rect.bottom.toFloat(),
                12f, 12f,
                paintTopicBox
            )

            val badgeText = "[${type.code}]"
            val textWidth = paintBadgeText.measureText(badgeText)
            val badgeWidth = textWidth + 30f
            val badgeHeight = 44f
            val badgeLeft = topic.rect.left.toFloat()
            val badgeTop = (topic.rect.top.toFloat() - badgeHeight - 6f).coerceAtLeast(10f)

            paintBadgeBg.color = type.color
            canvas.drawRoundRect(
                badgeLeft,
                badgeTop,
                badgeLeft + badgeWidth,
                badgeTop + badgeHeight,
                10f, 10f,
                paintBadgeBg
            )

            canvas.drawText(badgeText, badgeLeft + 15f, badgeTop + 30f, paintBadgeText)

            // Draw Word Box Container Box
            topic.wordBoxRect?.let { rect ->
                paintWordBoxContainer.color = 0xFFFF3366.toInt()
                canvas.drawRoundRect(
                    rect.left.toFloat(),
                    rect.top.toFloat(),
                    rect.right.toFloat(),
                    rect.bottom.toFloat(),
                    16f, 16f,
                    paintWordBoxContainer
                )
            }

            // Draw Word Grid Container Box
            topic.gridBoxRect?.let { rect ->
                paintGridContainer.color = 0xFF00FF66.toInt()
                canvas.drawRoundRect(
                    rect.left.toFloat(),
                    rect.top.toFloat(),
                    rect.right.toFloat(),
                    rect.bottom.toFloat(),
                    20f, 20f,
                    paintGridContainer
                )
            }
        }
    }
}

// Fullscreen interactive zone calibrator
class ZoneEditorView(context: Context) : View(context) {
    var topicTop = 0.05f
    var topicBottom = 0.14f
    var wordsTop = 0.15f
    var wordsBottom = 0.40f
    var gridTop = 0.42f
    var gridBottom = 0.88f

    private val paintTopic = Paint().apply {
        color = 0xFFFF8800.toInt() // Orange for Topic Zone
        strokeWidth = 6f
        style = Paint.Style.STROKE
        isAntiAlias = true
    }

    private val paintWords = Paint().apply {
        color = 0xFFFF4444.toInt() // Red for Words Zone
        strokeWidth = 6f
        style = Paint.Style.STROKE
        isAntiAlias = true
    }

    private val paintGrid = Paint().apply {
        color = 0xFF44FF44.toInt() // Green for Grid Zone
        strokeWidth = 6f
        style = Paint.Style.STROKE
        isAntiAlias = true
    }

    private val paintText = Paint().apply {
        color = 0xFFFFFFFF.toInt()
        textSize = 32f
        typeface = Typeface.DEFAULT_BOLD
        isAntiAlias = true
    }

    private val paintLabelBg = Paint().apply {
        color = 0xCC000000.toInt()
        style = Paint.Style.FILL
    }

    private var activeLine = -1 // 0: topicTop, 1: topicBottom, 2: wordsTop, 3: wordsBottom, 4: gridTop, 5: gridBottom

    override fun onDraw(canvas: Canvas) {
        super.onDraw(canvas)
        val w = width.toFloat()
        val h = height.toFloat()

        val yTT = topicTop * h
        val yTB = topicBottom * h
        val yWT = wordsTop * h
        val yWB = wordsBottom * h
        val yGT = gridTop * h
        val yGB = gridBottom * h

        // Draw light translucent backdrops showing the active scan zones
        canvas.drawRect(0f, yTT, w, yTB, Paint().apply { color = 0x22FF8800.toInt() })
        canvas.drawRect(0f, yWT, w, yWB, Paint().apply { color = 0x22FF4444.toInt() })
        canvas.drawRect(0f, yGT, w, yGB, Paint().apply { color = 0x2244FF44.toInt() })

        // Draw horizontal crop boundary lines
        canvas.drawLine(0f, yTT, w, yTT, paintTopic)
        canvas.drawLine(0f, yTB, w, yTB, paintTopic)
        canvas.drawLine(0f, yWT, w, yWT, paintWords)
        canvas.drawLine(0f, yWB, w, yWB, paintWords)
        canvas.drawLine(0f, yGT, w, yGT, paintGrid)
        canvas.drawLine(0f, yGB, w, yGB, paintGrid)

        // Draw indicator labels
        drawLabel(canvas, "TOPIC ZONE TOP", 40f, yTT, paintTopic.color)
        drawLabel(canvas, "TOPIC ZONE BOTTOM", 40f, yTB, paintTopic.color)
        drawLabel(canvas, "WORDS ZONE TOP", 40f, yWT, paintWords.color)
        drawLabel(canvas, "WORDS ZONE BOTTOM", 40f, yWB, paintWords.color)
        drawLabel(canvas, "GRID ZONE TOP", 40f, yGT, paintGrid.color)
        drawLabel(canvas, "GRID ZONE BOTTOM", 40f, yGB, paintGrid.color)
    }

    private fun drawLabel(canvas: Canvas, text: String, x: Float, y: Float, color: Int) {
        val textWidth = paintText.measureText(text)
        val textHeight = paintText.descent() - paintText.ascent()
        
        // Draw black background backing for maximum legibility
        canvas.drawRect(x - 10, y - textHeight - 8, x + textWidth + 10, y + 8, paintLabelBg)
        
        paintText.color = color
        canvas.drawText(text, x, y - paintText.descent() + 4, paintText)
    }

    override fun onTouchEvent(event: MotionEvent): Boolean {
        val h = height.toFloat()
        val y = event.y

        when (event.action) {
            MotionEvent.ACTION_DOWN -> {
                // Find closest line
                val dTT = Math.abs(y - topicTop * h)
                val dTB = Math.abs(y - topicBottom * h)
                val dWT = Math.abs(y - wordsTop * h)
                val dWB = Math.abs(y - wordsBottom * h)
                val dGT = Math.abs(y - gridTop * h)
                val dGB = Math.abs(y - gridBottom * h)

                val min = listOf(dTT, dTB, dWT, dWB, dGT, dGB).minOrNull() ?: Float.MAX_VALUE
                if (min < 90f) { // Touch target margin of 90 pixels
                    activeLine = when (min) {
                        dTT -> 0
                        dTB -> 1
                        dWT -> 2
                        dWB -> 3
                        dGT -> 4
                        dGB -> 5
                        else -> -1
                    }
                    return true
                }
            }
            MotionEvent.ACTION_MOVE -> {
                if (activeLine != -1) {
                    val ratio = (y / h).coerceIn(0f, 1f)
                    when (activeLine) {
                        0 -> topicTop = ratio.coerceAtMost(topicBottom - 0.02f)
                        1 -> topicBottom = ratio.coerceIn(topicTop + 0.02f, wordsTop - 0.02f)
                        2 -> wordsTop = ratio.coerceIn(topicBottom + 0.02f, wordsBottom - 0.02f)
                        3 -> wordsBottom = ratio.coerceIn(wordsTop + 0.02f, gridTop - 0.02f)
                        4 -> gridTop = ratio.coerceIn(wordsBottom + 0.02f, gridBottom - 0.02f)
                        5 -> gridBottom = ratio.coerceAtLeast(gridTop + 0.02f)
                    }
                    invalidate()
                    return true
                }
            }
            MotionEvent.ACTION_UP -> {
                activeLine = -1
            }
        }
        return super.onTouchEvent(event)
    }
}
