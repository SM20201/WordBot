    private fun fetchWordsFromDatamuse(topic: String): List<String> {
        val wordsList = mutableListOf<String>()
        try {
            // Encode the topic for URLs
            val encodedTopic = java.net.URLEncoder.encode(topic.lowercase(), "UTF-8")
            val urlString = "https://api.datamuse.com/words?ml=$encodedTopic&max=120"
            val url = java.net.URL(urlString)
            val conn = url.openConnection() as java.net.HttpURLConnection
            conn.requestMethod = "GET"
            conn.connectTimeout = 5000
            conn.readTimeout = 5000
            
            val responseCode = conn.responseCode
            if (responseCode == 200) {
                val json = conn.inputStream.bufferedReader().use { it.readText() }
                // Parse simple JSON array: [{"word":"zebra","score":1234}, ...]
                val regex = "\"word\":\"([a-zA-Z\\s-]+)\"".toRegex()
                val matches = regex.findAll(json)
                for (match in matches) {
                    val w = match.groupValues[1].uppercase().replace("\\s+".toRegex(), "").filter { it in 'A'..'Z' }
                    if (w.length >= 3) {
                        wordsList.add(w)
                    }
                }
            } else {
                Log.w(TAG, "Datamuse API returned error code: $responseCode")
            }
            conn.disconnect()
        } catch (e: Exception) {
            Log.e(TAG, "Failed to fetch words from Datamuse: ${e.message}", e)
        }
        
        Log.d(TAG, "Fetched ${wordsList.size} candidate words from Datamuse for topic '$topic'")
        return wordsList
    }

    private fun getWordsForTopic(topic: String): List<String> {
        val cleaned = topic.uppercase().trim()
        if (cleaned.isEmpty()) return topicDictionaries["COMMON"] ?: emptyList()
        
        // Find best matching category key from local dictionary
        for ((key, words) in topicDictionaries) {
            if (key != "COMMON" && cleaned.contains(key)) {
                Log.d(TAG, "Matching topic dictionary category: '$key'")
                return words
            }
        }
        
        // Fetch from Datamuse only if topic is a concise phrase (< 35 chars)
        if (cleaned.length in 3..35) {
            Log.d(TAG, "Fetching semantically related words for topic: '$cleaned'")
            val datamuseWords = fetchWordsFromDatamuse(cleaned)
            if (datamuseWords.isNotEmpty()) {
                return datamuseWords
            }
        }
        
        Log.d(TAG, "No specific topic category matched. Falling back to COMMON words list.")
        return topicDictionaries["COMMON"] ?: emptyList()
    }

