    private fun isWordAlreadySolved(bitmap: Bitmap, box: Rect): Boolean {
        val left = Math.max(0, box.left)
        val top = Math.max(0, box.top)
        val right = Math.min(bitmap.width - 1, box.right)
        val bottom = Math.min(bitmap.height - 1, box.bottom)
        
        if (right <= left || bottom <= top) return false
        
        var minBrightness = 255
        var maxBrightness = 0
        var count = 0
        
        val stepX = Math.max(1, (right - left) / 10)
        val stepY = Math.max(1, (bottom - top) / 6)
        
        for (x in left until right step stepX) {
            for (y in top until bottom step stepY) {
                val pixel = bitmap.getPixel(x, y)
                val r = (pixel shr 16) and 0xff
                val g = (pixel shr 8) and 0xff
                val b = pixel and 0xff
                val brightness = (r + g + b) / 3
                
                if (brightness < minBrightness) minBrightness = brightness
                if (brightness > maxBrightness) maxBrightness = brightness
                count++
            }
        }
        
        if (count == 0) return false
        val contrast = maxBrightness - minBrightness
        Log.d(TAG, "Contrast check for box $box: contrast=$contrast")
        return contrast < 85
    }

    /**
     * Rewritten auto zone detector.
     *
     * Strategy:
     * 1. Sample multiple columns (not just 3) to build a robust white-row map.
     * 2. Require a row to be white in the majority of sampled columns before counting it.
     * 3. After finding white segments, classify them using their RELATIVE positions
     *    to each other rather than fixed ratio thresholds — so the word box is always
     *    the uppermost large white segment, and the grid is the next one below.
     * 4. Shrink each detected segment inward by a small margin so its boundaries
     *    don't accidentally include the first/last rows of the adjacent zone.
     * 5. Fall back to OCR text clustering if pixel scanning produces less than 2 segments.
     */
    private fun autoDetectZonesAndTheme(visionText: com.google.mlkit.vision.text.Text, screenHeight: Int, bitmap: Bitmap) {
        val numY = bitmap.height
        val numX = bitmap.width

        // --- Step 1: build per-row white vote using 7 equally-spaced X columns ---
        val sampleXs = (1..7).map { i -> numX * i / 8 }
        val whiteVote = IntArray(numY)
        for (y in 0 until numY) {
            var votes = 0
            for (x in sampleXs) {
                val p = bitmap.getPixel(x, y)
                val r = (p shr 16) and 0xff
                val g = (p shr 8) and 0xff
                val b = p and 0xff
                // Accept near-white (r,g,b all >= 210) OR very light gray
                if (r >= 210 && g >= 210 && b >= 210) votes++
            }
            whiteVote[y] = votes
        }
        // A row counts as "white" when >= 5 of 7 sample columns are white
        val whiteThreshold = 5

        // --- Step 2: gather contiguous white segments >= 4% screen height ---
        val minSegLen = (numY * 0.04f).toInt()
        val segments = mutableListOf<Pair<Int, Int>>()
        var segStart = -1
        for (y in 0 until numY) {
            val isWhite = whiteVote[y] >= whiteThreshold
            if (isWhite) {
                if (segStart == -1) segStart = y
            } else {
                if (segStart != -1) {
                    val len = y - segStart
                    if (len >= minSegLen) segments.add(Pair(segStart, y - 1))
                    segStart = -1
                }
            }
        }
        if (segStart != -1 && (numY - segStart) >= minSegLen) {
            segments.add(Pair(segStart, numY - 1))
        }

        // Sort segments top-to-bottom
        val sortedSegs = segments.sortedBy { it.first }
        Log.d(TAG, "AutoZone: found ${sortedSegs.size} white segments: $sortedSegs")

        var headerMinY = 0.03f
        var headerMaxY = 0.13f
        var wordsMinY  = 0.14f
        var wordsMaxY  = 0.38f
        var gridMinY   = 0.40f
        var gridMaxY   = 0.90f

        if (sortedSegs.size >= 2) {
            // Pick the two largest segments — first (topmost) is word box, second is grid
            val bySize = sortedSegs.sortedByDescending { it.second - it.first }
            val wordSeg = bySize.minByOrNull { it.first }!!      // topmost of the two largest
            val gridSeg = bySize.filter { it != wordSeg }.minByOrNull { it.first } ?: bySize[1]

            // Shrink each segment inward by 1% of screen height so we don't clip into borders
            val shrink = (numY * 0.012f).toInt()
            val wTop    = (wordSeg.first  + shrink).toFloat() / numY
            val wBottom = (wordSeg.second - shrink).toFloat() / numY
            val gTop    = (gridSeg.first  + shrink).toFloat() / numY
            val gBottom = (gridSeg.second - shrink).toFloat() / numY

            // Header is the area above the word box
            headerMinY = 0.02f
            headerMaxY = (wordSeg.first.toFloat() / numY - 0.01f).coerceAtLeast(0.03f)
            wordsMinY  = wTop.coerceAtLeast(headerMaxY + 0.01f)
            wordsMaxY  = wBottom.coerceAtMost(gTop - 0.01f)
            gridMinY   = gTop.coerceAtLeast(wordsMaxY + 0.01f)
            gridMaxY   = gBottom.coerceAtMost(0.97f)

            Log.d(TAG, "AutoZone: pixel scan OK — wordSeg=$wordSeg gridSeg=$gridSeg")
        } else {
            // --- Fallback: OCR text-line clustering ---
            Log.d(TAG, "AutoZone: <2 segments found, falling back to OCR clustering")
            val allLines = visionText.textBlocks.flatMap { it.lines }

            if (allLines.isNotEmpty()) {
                // Identify header lines (top ~15% of screen)
                val headerLines = allLines.filter {
                    val r = (it.boundingBox?.centerY() ?: 0).toFloat() / screenHeight
                    r in 0.01f..0.16f
                }
                if (headerLines.isNotEmpty()) {
                    val ys = headerLines.map { (it.boundingBox?.centerY() ?: 0).toFloat() / screenHeight }
                    headerMinY = (ys.minOrNull() ?: 0.03f) - 0.015f
                    headerMaxY = (ys.maxOrNull() ?: 0.13f) + 0.015f
                }

                // Identify grid lines: single-letter OCR elements spaced regularly in bottom 60%
                val gridLines = allLines.filter {
                    val r = (it.boundingBox?.centerY() ?: 0).toFloat() / screenHeight
                    r in 0.35f..0.97f
                }
                if (gridLines.isNotEmpty()) {
                    val ys = gridLines.map { (it.boundingBox?.centerY() ?: 0).toFloat() / screenHeight }
                    gridMinY = (ys.minOrNull() ?: 0.40f) - 0.015f
                    gridMaxY = (ys.maxOrNull() ?: 0.90f) + 0.015f
                }

                // Word box: between header and grid
                val midLines = allLines.filter {
                    val r = (it.boundingBox?.centerY() ?: 0).toFloat() / screenHeight
                    r in (headerMaxY + 0.005f)..(gridMinY - 0.005f)
                }
                if (midLines.isNotEmpty()) {
                    val ys = midLines.map { (it.boundingBox?.centerY() ?: 0).toFloat() / screenHeight }
                    wordsMinY = (ys.minOrNull() ?: 0.14f) - 0.015f
                    wordsMaxY = (ys.maxOrNull() ?: 0.38f) + 0.015f
                }
            }
        }

        // Final clamp & gap enforcement
        topicTopY    = headerMinY.coerceIn(0.01f, 0.18f)
        topicBottomY = headerMaxY.coerceIn(topicTopY + 0.02f, 0.22f)
        wordsTopY    = wordsMinY.coerceIn(topicBottomY + 0.01f, 0.44f)
        wordsBottomY = wordsMaxY.coerceIn(wordsTopY + 0.02f, 0.52f)
        gridTopY     = gridMinY.coerceIn(wordsBottomY + 0.01f, 0.80f)
        gridBottomY  = gridMaxY.coerceIn(gridTopY + 0.04f, 0.97f)

        Log.d(TAG, "AutoZone result: Topic($topicTopY-$topicBottomY) Words($wordsTopY-$wordsBottomY) Grid($gridTopY-$gridBottomY)")

        // Theme brightness detection (unchanged)
        var totalBrightness = 0L
        var sampleCount = 0
        val stepX = (bitmap.width / 20).coerceAtLeast(1)
        val stepY2 = (bitmap.height / 20).coerceAtLeast(1)
        for (x in 0 until bitmap.width step stepX) {
            for (y in 0 until bitmap.height step stepY2) {
                val pixel = bitmap.getPixel(x, y)
                val r = (pixel shr 16) and 0xff
                val g = (pixel shr 8) and 0xff
                val b = pixel and 0xff
                totalBrightness += (r + g + b) / 3
                sampleCount++
            }
        }
        if (sampleCount > 0) {
            val avg = totalBrightness / sampleCount
            Log.d(TAG, "AutoZone theme brightness: $avg -> ${if (avg > 120) "LIGHT" else "DARK"}")
        }
    }

    private fun fetchSynonymsOrAntonyms(word: String, type: String): List<String> {
        val wordsList = mutableListOf<String>()
        try {
            val urlStr = "https://api.datamuse.com/words?rel_$type=${word.lowercase()}&max=50"
            val url = URL(urlStr)
            val conn = url.openConnection() as java.net.HttpURLConnection
            conn.connectTimeout = 3000
            conn.readTimeout = 3000
            conn.requestMethod = "GET"
            
            val responseCode = conn.responseCode
            if (responseCode == 200) {
                val response = conn.inputStream.bufferedReader().use { it.readText() }
                val regex = "\"word\":\"([a-zA-Z\\s-]+)\"".toRegex()
                val matches = regex.findAll(response)
                for (match in matches) {
                    val candidate = match.groupValues[1].uppercase().replace("\\s+".toRegex(), "")
                    if (candidate.length >= 3 && candidate.all { it.isLetter() }) {
                        wordsList.add(candidate)
                    }
                }
            }
            conn.disconnect()
        } catch (e: Exception) {
            Log.e(TAG, "Failed to fetch $type for '$word': ${e.message}", e)
        }
        return wordsList
    }



    private fun getMajorityCoordinate(coords: List<Float>): Float {
        if (coords.isEmpty()) return 0f
        var bestCoord = coords[0]
        var maxCount = 0
        for (c in coords) {
            val count = coords.count { Math.abs(it - c) < 15f }
            if (count > maxCount) {
                maxCount = count
                bestCoord = c
            }
        }
        return bestCoord
    }

    private fun rebuildSnappedGrid(detectedLetters: List<Letter>, screenHeight: Int): Pair<List<List<Letter>>, List<Letter>> {
        if (detectedLetters.isEmpty()) return Pair(emptyList(), emptyList())

        val defaultStep = screenHeight * 0.055f // ~132px on 2400px screen
        val widths = detectedLetters.filter { it.width > 0f }.map { it.width }.sorted()
        val heights = detectedLetters.filter { it.height > 0f }.map { it.height }.sorted()
        
        val medianWidth = if (widths.isNotEmpty()) widths[widths.size / 2] else defaultStep
        val medianHeight = if (heights.isNotEmpty()) heights[heights.size / 2] else defaultStep

        val initialRowThreshold = medianHeight * 0.6f
        val initialColThreshold = medianWidth * 0.6f

        // 1. Cluster Y coordinates into rows
        val rows = mutableListOf<MutableList<Letter>>()
        val sortedLettersByY = detectedLetters.sortedBy { it.y }
        for (letter in sortedLettersByY) {
            if (rows.isEmpty() || letter.y - rows.last().last().y > initialRowThreshold) {
                rows.add(mutableListOf(letter))
            } else {
                rows.last().add(letter)
            }
        }

        // Find expected row size (width of grid)
        val rowSizes = rows.map { it.size }.sorted()
        val expectedWidth = if (rowSizes.isNotEmpty()) rowSizes[rowSizes.size * 3 / 4] else 0
        val minRowSize = Math.max(3, (expectedWidth * 0.45f).toInt())

        // Filter out outlier rows
        val filteredRows = rows.filter { it.size >= minRowSize }
        val initialRowYs = filteredRows.map { r -> getMajorityCoordinate(r.map { it.y }) }.sorted()

        // Estimate rowHeight from initialRowYs gaps
        val yGaps = mutableListOf<Float>()
        for (i in 0 until initialRowYs.size - 1) {
            val gap = initialRowYs[i+1] - initialRowYs[i]
            if (gap > initialRowThreshold) {
                yGaps.add(gap)
            }
        }
        val rowHeight = if (yGaps.isNotEmpty()) yGaps.sorted()[yGaps.size / 2] else defaultStep

        // 2. Cluster X coordinates into columns
        val cols = mutableListOf<MutableList<Letter>>()
        val sortedLettersByX = detectedLetters.sortedBy { it.x }
        for (letter in sortedLettersByX) {
            if (cols.isEmpty() || letter.x - cols.last().last().x > initialColThreshold) {
                cols.add(mutableListOf(letter))
            } else {
                cols.last().add(letter)
            }
        }

        // Find expected col size (height of grid)
        val colSizes = cols.map { it.size }.sorted()
        val expectedHeight = if (colSizes.isNotEmpty()) colSizes[colSizes.size * 3 / 4] else 0
        val minColSize = Math.max(3, (expectedHeight * 0.45f).toInt())

        // Filter out outlier columns
        val filteredCols = cols.filter { it.size >= minColSize }
        val initialColXs = filteredCols.map { c -> getMajorityCoordinate(c.map { it.x }) }.sorted()

        // Estimate colWidth from initialColXs gaps
        val xGaps = mutableListOf<Float>()
        for (i in 0 until initialColXs.size - 1) {
            val gap = initialColXs[i+1] - initialColXs[i]
            if (gap > initialColThreshold) {
                xGaps.add(gap)
            }
        }
        val colWidth = if (xGaps.isNotEmpty()) xGaps.sorted()[xGaps.size / 2] else defaultStep

        // 3. Fill in missing rows
        val finalRowYs = mutableListOf<Float>()
        if (initialRowYs.isNotEmpty()) {
            finalRowYs.add(initialRowYs[0])
            for (i in 0 until initialRowYs.size - 1) {
                val y1 = initialRowYs[i]
                val y2 = initialRowYs[i+1]
                val gap = y2 - y1
                val numMissing = Math.round(gap / rowHeight) - 1
                if (numMissing > 0) {
                    val step = gap / (numMissing + 1)
                    for (j in 1..numMissing) {
                        finalRowYs.add(y1 + step * j)
                    }
                }
                finalRowYs.add(y2)
            }
        }

        // 4. Fill in missing columns
        val finalColXs = mutableListOf<Float>()
        if (initialColXs.isNotEmpty()) {
            finalColXs.add(initialColXs[0])
            for (i in 0 until initialColXs.size - 1) {
                val x1 = initialColXs[i]
                val x2 = initialColXs[i+1]
                val gap = x2 - x1
                val numMissing = Math.round(gap / colWidth) - 1
                if (numMissing > 0) {
                    val step = gap / (numMissing + 1)
                    for (j in 1..numMissing) {
                        finalColXs.add(x1 + step * j)
                    }
                }
                finalColXs.add(x2)
            }
        }

        // 5. Map letters to the grid structure
        val numRows = finalRowYs.size
        val numCols = finalColXs.size
        val gridMap = Array(numRows) { Array<Letter?>(numCols) { null } }

        for (letter in detectedLetters) {
            var closestRow = -1
            var minDistY = Float.MAX_VALUE
            for (r in 0 until numRows) {
                val distY = Math.abs(letter.y - finalRowYs[r])
                if (distY < minDistY) {
                    minDistY = distY
                    closestRow = r
                }
            }

            var closestCol = -1
            var minDistX = Float.MAX_VALUE
            for (c in 0 until numCols) {
                val distX = Math.abs(letter.x - finalColXs[c])
                if (distX < minDistX) {
                    minDistX = distX
                    closestCol = c
                }
            }

            if (closestRow != -1 && closestCol != -1) {
                val maxAllowedDistX = colWidth * 0.95f
                val maxAllowedDistY = rowHeight * 0.95f
                if (minDistX < maxAllowedDistX && minDistY < maxAllowedDistY) {
                    val existing = gridMap[closestRow][closestCol]
                    if (existing == null) {
                        gridMap[closestRow][closestCol] = Letter(letter.char, finalColXs[closestCol], finalRowYs[closestRow])
                    } else {
                        val eChar = existing.char.uppercaseChar()
                        val lChar = letter.char.uppercaseChar()
                        // Automatically recombine 'Q' if OCR split it into 'O' + 'I'/'/'
                        if ((eChar == 'O' && lChar in listOf('I', 'L', '/', '\\', '|', '!')) ||
                            (lChar == 'O' && eChar in listOf('I', 'L', '/', '\\', '|', '!'))) {
                            gridMap[closestRow][closestCol] = Letter('Q', finalColXs[closestCol], finalRowYs[closestRow])
                        } else {
                            val distExisting = Math.hypot(
                                (existing.x - letter.x).toDouble(),
                                (existing.y - letter.y).toDouble()
                            )
                            val distNew = Math.hypot(
                                (finalColXs[closestCol] - letter.x).toDouble(),
                                (finalRowYs[closestRow] - letter.y).toDouble()
                            )
                            if (distNew < distExisting) {
                                gridMap[closestRow][closestCol] = Letter(letter.char, finalColXs[closestCol], finalRowYs[closestRow])
                            }
                        }
                    }
                }
            }
        }

        // 6. Build final structure
        val gridList = mutableListOf<List<Letter>>()
        val flatSnapped = mutableListOf<Letter>()
        for (r in 0 until numRows) {
            val rowLetters = mutableListOf<Letter>()
            for (c in 0 until numCols) {
                val letter = gridMap[r][c] ?: Letter('?', finalColXs[c], finalRowYs[r])
                rowLetters.add(letter)
                if (gridMap[r][c] != null) {
                    flatSnapped.add(letter)
                }
            }
            gridList.add(rowLetters)
        }

        return Pair(gridList, flatSnapped)
    }

    private fun startSolvingFlow() {
        if (isSolving) return
        isSolving = true
        enableSolveButtons(false)
        updateStatus("Capturing screen...", true)

        captureScreen { bitmap ->
            if (bitmap == null) {
                updateStatus("Capture failed!")
                isSolving = false
                enableSolveButtons(true)
                return@captureScreen
            }

            updateStatus("Recognizing grid & words...", true)

            // Pass the original bitmap directly to ML Kit (no destructive binarization!)
            val image = InputImage.fromBitmap(bitmap, 0)
            val recognizer = TextRecognition.getClient(TextRecognizerOptions.DEFAULT_OPTIONS)

            val metrics = DisplayMetrics()
            windowManager.defaultDisplay.getRealMetrics(metrics)
            val screenHeight = metrics.heightPixels

            recognizer.process(image)
                .addOnSuccessListener { visionText ->
                    Thread {
                        // A. Check for auto-click buttons if enabled
                        if (cbAutoClick.isChecked) {
                            var clickedAction = false
                            for (block in visionText.textBlocks) {
                                for (line in block.lines) {
                                     val text = line.text.uppercase().trim()
                                     val matchedKeyword = autoClickKeywords.find { keyword ->
                                         text == keyword || text.contains(keyword)
                                     }
                                     if (matchedKeyword != null) {
                                         val box = line.boundingBox
                                         if (box != null) {
                                             val clickX = box.centerX().toFloat()
                                             val clickY = box.centerY().toFloat()
                                             Log.d(TAG, "Auto-click action triggered for keyword '$matchedKeyword' at ($clickX, $clickY)")
                                             updateStatus("Auto-clicking '$matchedKeyword'...", true)
                                             
                                             simulateClick(clickX, clickY)
                                             clickedAction = true
                                             break
                                         }
                                     }
                                }
                                if (clickedAction) break
                            }
                            
                            if (clickedAction) {
                                bitmap.recycle()
                                Thread.sleep(2500)
                                isSolving = false
                                enableSolveButtons(true)
                                
                                if (cbAutoMode.isChecked) {
                                    mainHandler.postDelayed({
                                        startSolvingFlow()
                                    }, 800)
                                }
                                return@Thread
                            }
                        }

                        // Extract level topic from Topic Zone using LevelOverlayEngine
                        val topicResult = levelOverlayEngine.detectLevelTopic(
                            visionText,
                            bitmap,
                            topicTopY,
                            topicBottomY,
                            wordsTopY,
                            wordsBottomY,
                            gridTopY,
                            gridBottomY,
                            bitmap.width,
                            screenHeight
                        )
                        val isGridEnumMode = topicResult.levelType != LevelType.REGULAR
                        val actualTopic = topicResult.cleanedTopicText
                        Log.d(TAG, "LevelOverlayEngine detected: Code='${topicResult.levelType.code}', Label='${topicResult.levelType.label}', Topic='$actualTopic'")
                        
                        val categoryWords = getWordsForTopic(actualTopic)
                        if (actualTopic != lastBruteForceTopic) {
                            lastBruteForceTopic = actualTopic
                            bruteForceTriedWords.clear()
                        }

                        // 1. Gather Target Words from screen OCR (inside crop zone boundaries)
                        val targetWords = mutableListOf<String>()
                        val targetWordBoxes = mutableListOf<Pair<String, Rect>>()
                        
                        // Always scan word zone for target words or hidden placeholders
                        for (block in visionText.textBlocks) {
                            for (line in block.lines) {
                                val lineBox = line.boundingBox ?: continue
                                val centerYRatio = lineBox.centerY().toFloat() / screenHeight
                                if (centerYRatio in wordsTopY..wordsBottomY) {
                                    // Use per-element bounding boxes for precise letter count and position
                                    for (element in line.elements) {
                                        val elemBox = element.boundingBox ?: continue
                                        val cleaned = cleanOcrWord(element.text)
                                        if (cleaned.length >= 3 && cleaned !in excludeWords) {
                                            targetWords.add(cleaned)
                                            targetWordBoxes.add(Pair(cleaned, elemBox))
                                        }
                                    }
                                }
                            }
                        }

                        val finalTargetWords = mutableListOf<String>()
                        val highlights = mutableListOf<WordHighlight>()
                        
                        if (targetWordBoxes.isNotEmpty()) {
                            for ((word, rect) in targetWordBoxes) {
                                val isSolved = isWordAlreadySolved(bitmap, rect)
                                val isSecret = isHiddenWordPlaceholder(word)
                                
                                val highlightColor = if (isSolved) {
                                    0xFF000000.toInt() // Black for solved
                                } else {
                                    0xFFFF0000.toInt() // Red for unsolved
                                }
                                
                                highlights.add(WordHighlight(rect, word, highlightColor, isSecret, word.length))
                                
                                if (!isSolved) {
                                    finalTargetWords.add(word)
                                }
                            }
                            Log.d(TAG, "Active unsolved words to search: $finalTargetWords")
                        } else {
                            // Fallback to text box if OCR didn't find any target words on screen
                            if (!cbAutoMode.isChecked && !cbBruteForce.isChecked) {
                                val wordsInput = etTargetWords.text.toString()
                                val parsed = wordsInput.split(",")
                                    .map { cleanOcrWord(it) }
                                    .filter { it.length >= 3 }
                                finalTargetWords.addAll(parsed)
                            }
                        }

                        // 2. Gather Letters from the Grid (inside crop bounds)
                        val rawLetters = mutableListOf<Letter>()
                        for (block in visionText.textBlocks) {
                            for (line in block.lines) {
                                val lineBox = line.boundingBox ?: continue
                                val centerYRatio = lineBox.centerY().toFloat() / screenHeight
                                if (centerYRatio in gridTopY..gridBottomY) {
                                    for (element in line.elements) {
                                        val elementBox = element.boundingBox ?: continue
                                        val text = element.text.uppercase().filter { it.isLetterOrDigit() || it in "|l/\\[]()!1.,:;-" }
                                        if (text.isEmpty()) continue

                                        val elementWidth = elementBox.width()
                                        val charWidth = elementWidth.toFloat() / text.length
                                        for (i in text.indices) {
                                            val rawC = text[i]
                                            val c = cleanGridChar(rawC)
                                            val charX = elementBox.left + charWidth * i + charWidth / 2f
                                            val charY = elementBox.centerY().toFloat()
                                            
                                            rawLetters.add(Letter(c, charX, charY, charWidth, elementBox.height().toFloat()))
                                        }
                                    }
                                }
                            }
                        }

                        // 3. Reconstruct perfect grid layout & snap coordinates
                        val (grid, flatSnapped) = rebuildSnappedGrid(rawLetters, screenHeight)

                        if (grid.isEmpty() || flatSnapped.size < 4) {
                            bitmap.recycle()
                            updateStatus("No grid detected. Stopping brute force.")
                            isSolving = false
                            enableSolveButtons(true)
                            return@Thread
                        }

                        // 4. Extract level topic from Topic Zone (Already extracted before step 1)

                        // 4.5. Guess/Extract words if brute force, special level, hidden words, or no target words detected
                        val needsBruteForceOrGuessing = isGridEnumMode || cbScanAllWords.isChecked || cbBruteForce.isChecked || finalTargetWords.isEmpty() || finalTargetWords.any { isHiddenWordPlaceholder(it) }
                        val gridWordsExtracted = if (grid.isNotEmpty()) findAllValidGridWords(grid) else emptyList()
                        val patternMatchedWords = if (grid.isNotEmpty() && needsBruteForceOrGuessing) {
                            findValidWordsMatchingGridPatterns(grid)
                        } else emptyList()

                        if (needsBruteForceOrGuessing) {
                            val mode = when {
                                rbModeCircle.isChecked -> SolveMode.CIRCLE
                                rbModeSnake.isChecked -> SolveMode.SNAKE
                                else -> SolveMode.STRAIGHT
                            }
                            
                            val allDbWords = if (cbBruteForce.isChecked || isGridEnumMode) (topicDictionaries.values.flatten() + extraCommonWords).distinct() else emptyList()
                            
                            // Target words on screen get TOP PRIORITY, followed by grid-extracted words, pattern-matched words, category words, and db words
                            val candidatesList = (finalTargetWords + gridWordsExtracted + patternMatchedWords + categoryWords + allDbWords).distinct()
                            
                            val foundWords = mutableListOf<String>()
                            val currentTime = System.currentTimeMillis()
                            
                            for (word in candidatesList) {
                                if (cbBruteForce.isChecked && bruteForceTriedWords.contains(word)) {
                                    continue
                                }
                                // Skip recently tried/swiped words
                                if (currentTime - (triedSwipesCache[word] ?: 0L) <= 14000L) {
                                    continue
                                }
                                
                                if (mode == SolveMode.CIRCLE) {
                                    val path = findCircleWord(word, rawLetters)
                                    if (path != null && isStrictMatch(path, word)) {
                                        foundWords.add(word)
                                    }
                                } else {
                                    if (grid.isNotEmpty()) {
                                        val path = if (mode == SolveMode.STRAIGHT) {
                                            findStraightWord(word, grid)
                                        } else {
                                            findSnakeWord(word, grid)
                                        }
                                        if (path != null && isStrictMatch(path, word)) {
                                            foundWords.add(word)
                                        }
                                    }
                                }
                            }
                            
                            if (foundWords.isNotEmpty()) {
                                for (w in foundWords) {
                                    if (!finalTargetWords.contains(w)) {
                                        finalTargetWords.add(w)
                                    }
                                }
                                Log.d(TAG, "Grid matches found: $finalTargetWords")
                            }
                        }

                        if (finalTargetWords.isEmpty()) {
                            bitmap.recycle()
                            updateStatus("All words solved!")
                            isSolving = false
                            enableSolveButtons(true)
                            
                            mainHandler.post {
                                if (cbHighlightLetters.isChecked) {
                                    updateLettersOverlay(emptyList(), highlights)
                                }
                            }

                            if (cbAutoMode.isChecked || cbBruteForce.isChecked) {
                                updateStatus("Auto/Brute loop: All solved, next scan in 1.5s...", true)
                                mainHandler.postDelayed({
                                    if (!isExpandedShown) {
                                        startSolvingFlow()
                                    }
                                }, 1500)
                            }
                            return@Thread
                        }

                        // Update UI text box if we found words on screen
                        mainHandler.post {
                            etTargetWords.setText(finalTargetWords.joinToString(", "))
                        }

                        // 5. Guess hidden/secret words (represented as "OOOOO" placeholders)
                        // Build the multiset of available grid letters so we only guess words
                        // that can actually be formed from the letters in the grid.
                        val gridLetterBag: Map<Char, Int> = flatSnapped
                            .filter { it.char in 'A'..'Z' }
                            .groupingBy { it.char }
                            .eachCount()

                        fun canFormFromGrid(word: String): Boolean {
                            val needed = word.groupingBy { it }.eachCount()
                            return needed.all { (ch, count) -> (gridLetterBag[ch] ?: 0) >= count }
                        }

                        val guessedTargetWords = finalTargetWords.map { word ->
                            if (isHiddenWordPlaceholder(word)) {
                                val currentTime = System.currentTimeMillis()
                                // Extract any length-matching words directly from grid lines first!
                                val gridLengthCandidates = if (grid.isNotEmpty()) {
                                    findAllValidGridWords(grid, filterLength = word.length)
                                } else emptyList()

                                val candidates = (gridLengthCandidates + categoryWords + extraCommonWords).filter { cand ->
                                    cand.length == word.length &&
                                    (currentTime - (triedSecretWordsCache[cand] ?: 0L) > 14000L) &&
                                    canFormFromGrid(cand)
                                }.distinct()
                                Log.d(TAG, "Attempting to guess hidden word of length ${word.length}. Candidates count: ${candidates.size}")
                                
                                var bestGuess: String? = null
                                var bestScore = Int.MAX_VALUE
                                
                                val mode = when {
                                    rbModeLine.isChecked -> SolveMode.STRAIGHT
                                    rbModeSnake.isChecked -> SolveMode.SNAKE
                                    rbModeCircle.isChecked -> SolveMode.CIRCLE
                                    else -> SolveMode.STRAIGHT
                                }
                                
                                for (cand in candidates) {
                                    if (mode == SolveMode.CIRCLE) {
                                        val path = findCircleWord(cand, rawLetters)
                                        if (path != null) {
                                            var score = 0
                                            for (i in cand.indices) {
                                                val gc = path[i].char
                                                val wc = cand[i]
                                                if (gc == '?') score += 2
                                                else {
                                                    val mt = getMatchType(gc, wc)
                                                    if (mt == 1) score += 1
                                                    else if (mt < 0) score += 3
                                                }
                                            }
                                            if (score < bestScore) {
                                                bestScore = score
                                                bestGuess = cand
                                            }
                                        }
                                    } else {
                                        if (grid.isNotEmpty()) {
                                            val path = if (mode == SolveMode.STRAIGHT) {
                                                findStraightWord(cand, grid)
                                            } else {
                                                findSnakeWord(cand, grid)
                                            }
                                            if (path != null) {
                                                var exactMatches = 0
                                                var fuzzyMatches = 0
                                                var wildcards = 0
                                                var mismatches = 0
                                                for (i in cand.indices) {
                                                    val gc = path[i].char
                                                    val wc = cand[i]
                                                    if (gc == '?') {
                                                        wildcards++
                                                    } else {
                                                        val mt = getMatchType(gc, wc)
                                                        when (mt) {
                                                            0 -> exactMatches++
                                                            1 -> fuzzyMatches++
                                                            else -> mismatches++
                                                        }
                                                    }
                                                }
                                                val score = exactMatches * 0 + fuzzyMatches * 1 + wildcards * 2 + mismatches * 4
                                                if (score < bestScore) {
                                                    bestScore = score
                                                    bestGuess = cand
                                                }
                                            }
                                        }
                                    }
                                }
                                if (bestGuess != null) {
                                    Log.d(TAG, "Successfully guessed hidden word: '$word' -> '$bestGuess' with score $bestScore")
                                    triedSecretWordsCache[bestGuess] = System.currentTimeMillis()
                                    bestGuess
                                } else {
                                    Log.d(TAG, "Failed to guess hidden word for placeholder '$word'")
                                    word
                                }
                            } else {
                                word
                            }
                        }

                        // 5.5. Map target words to synonyms or antonyms if enabled
                        val finalSearchWords = mutableListOf<String>()
                        val isSynonyms = cbDiffSynonyms.isChecked
                        val isAntonyms = cbDiffAntonyms.isChecked
                        
                        for (target in guessedTargetWords) {
                            if (isSynonyms || isAntonyms) {
                                val relType = if (isSynonyms) "syn" else "ant"
                                updateStatus("Fetching ${relType} for '$target'...", true)
                                val candidates = fetchSynonymsOrAntonyms(target, relType)
                                Log.d(TAG, "Candidates ($relType) for '$target': $candidates")
                                
                                var matchedWord: String? = null
                                val mode = when {
                                    rbModeLine.isChecked -> SolveMode.STRAIGHT
                                    rbModeSnake.isChecked -> SolveMode.SNAKE
                                    rbModeCircle.isChecked -> SolveMode.CIRCLE
                                    else -> SolveMode.STRAIGHT
                                }
                                
                                val currentTime = System.currentTimeMillis()
                                for (cand in candidates) {
                                    if (currentTime - (triedSwipesCache[cand] ?: 0L) <= 14000L) {
                                        continue
                                    }
                                    if (mode == SolveMode.CIRCLE) {
                                        val path = findCircleWord(cand, rawLetters)
                                        if (path != null && isStrictMatch(path, cand)) {
                                            matchedWord = cand
                                            break
                                        }
                                    } else {
                                        if (grid.isNotEmpty()) {
                                            val path = if (mode == SolveMode.STRAIGHT) {
                                                findStraightWord(cand, grid)
                                            } else {
                                                findSnakeWord(cand, grid)
                                            }
                                            if (path != null && isStrictMatch(path, cand)) {
                                                matchedWord = cand
                                                break
                                            }
                                        }
                                    }
                                }
                                
                                if (matchedWord != null) {
                                    Log.d(TAG, "Resolved target '$target' to grid word '$matchedWord'")
                                    finalSearchWords.add(matchedWord)
                                } else {
                                    Log.d(TAG, "Could not resolve target '$target' on the grid, searching target directly.")
                                    finalSearchWords.add(target)
                                }
                            } else {
                                finalSearchWords.add(target)
                            }
                        }

                        // Update UI text field if any word was guessed/resolved
                        if (finalSearchWords != finalTargetWords) {
                            mainHandler.post {
                                etTargetWords.setText(finalSearchWords.joinToString(", "))
                            }
                        }

                        // Recycle the captured screen bitmap now that we have finished reading pixels
                        bitmap.recycle()

                        // 6. Determine Solve Mode
                        val mode = when {
                            rbModeLine.isChecked -> SolveMode.STRAIGHT
                            rbModeSnake.isChecked -> SolveMode.SNAKE
                            rbModeCircle.isChecked -> SolveMode.CIRCLE
                            else -> SolveMode.STRAIGHT
                        }
                        Log.d(TAG, "Current Solve Mode Selected: $mode")

                        if (mode == SolveMode.CIRCLE) {
                            val detectedLetters = mutableListOf<Letter>()
                            for (l in rawLetters) {
                                var isDuplicate = false
                                for (existing in detectedLetters) {
                                    val dist = Math.hypot((l.x - existing.x).toDouble(), (l.y - existing.y).toDouble())
                                    if (dist < 32.0) {
                                        isDuplicate = true
                                        break
                                    }
                                }
                                if (!isDuplicate) {
                                    detectedLetters.add(l)
                                }
                            }

                            Log.d(TAG, "===== SOLVING PROCESS DIAGNOSTICS (WHEEL) =====")
                            Log.d(TAG, "Target Words to Solve: $finalSearchWords")
                            Log.d(TAG, "Detected Wheel Letters Count: ${detectedLetters.size}")
                            for (l in detectedLetters) {
                                Log.d(TAG, "Wheel Letter '${l.char}' at (${l.x}, ${l.y})")
                            }

                            updateLettersOverlay(detectedLetters, highlights)

                            if (detectedLetters.isEmpty()) {
                                updateStatus("No wheel letters detected.")
                                isSolving = false
                                enableSolveButtons(true)
                                return@Thread
                            }

                            updateStatus("Solving wheel words...", true)
                            solveCircleAndSwipe(finalSearchWords, detectedLetters)
                        } else {
                            Log.d(TAG, "===== SOLVING PROCESS DIAGNOSTICS (GRID) =====")
                            Log.d(TAG, "Target Words to Solve: $finalSearchWords")
                            Log.d(TAG, "Detected Grid Letters Count (De-duplicated & Snapped): ${flatSnapped.size}")
                            
                            val gridStr = grid.joinToString("\n") { r -> r.map { it.char }.joinToString(" ") }
                            Log.d(TAG, "Rebuilt Grid Layout Matrix:\n$gridStr")

                            updateLettersOverlay(flatSnapped, highlights, topicResult)

                            if (flatSnapped.isEmpty()) {
                                updateStatus("No grid letters detected.")
                                isSolving = false
                                enableSolveButtons(true)
                                return@Thread
                            }

                            updateStatus("Solving grid words...", true)
                            solveGridAndSwipe(finalSearchWords, grid, mode)
                        }
                    }.start()
                }
                .addOnFailureListener { e ->
                    bitmap.recycle()
                    e.printStackTrace()
                    updateStatus("OCR failed: ${e.message}")
                    isSolving = false
                    enableSolveButtons(true)
                }
        }
    }

    private fun solveCircleAndSwipe(words: List<String>, letters: List<Letter>) {
        val solvedPaths = mutableListOf<List<Letter>>()

        val currentTime = System.currentTimeMillis()
        for (word in words) {
            if (cbBruteForce.isChecked && bruteForceTriedWords.contains(word)) {
                continue
            }
            if (currentTime - (triedSwipesCache[word] ?: 0L) <= 14000L) {
                Log.d(TAG, "Word '$word' was already swiped/tried recently. Skipping.")
                continue
            }
            val path = findCircleWord(word, letters)
            if (path != null) {
                solvedPaths.add(path)
                triedSwipesCache[word] = currentTime
                if (cbBruteForce.isChecked) {
                    bruteForceTriedWords.add(word)
                }
                Log.d(TAG, "Word '$word' found in Wheel. Letter path: ${path.map { "${it.char}(${it.x},${it.y})" }}")
            } else {
                Log.d(TAG, "Word '$word' NOT found in Wheel.")
            }
        }

        if (solvedPaths.isEmpty()) {
            updateStatus("Solved 0/${words.size} wheel words.")
            isSolving = false
            enableSolveButtons(true)
            if (cbAutoMode.isChecked || cbBruteForce.isChecked) {
                updateStatus("Auto/Brute loop: 0 solved, next scan in 1.5s...", true)
                mainHandler.postDelayed({
                    if (!isExpandedShown) {
                        startSolvingFlow()
                    }
                }, 1500)
            }
            return
        }

        updateStatus("Found ${solvedPaths.size}/${words.size} words. Swiping...", true)

        val swipeCoordinates = solvedPaths.map { path ->
            path.map { letter -> Pair(letter.x, letter.y) }
        }

        executeSequentialSwipes(swipeCoordinates)
    }

    private fun solveGridAndSwipe(words: List<String>, grid: List<List<Letter>>, mode: SolveMode) {
        val solvedPaths = mutableListOf<List<Letter>>()

        val currentTime = System.currentTimeMillis()
        for (word in words) {
            if (cbBruteForce.isChecked && bruteForceTriedWords.contains(word)) {
                continue
            }
            if (currentTime - (triedSwipesCache[word] ?: 0L) <= 14000L) {
                Log.d(TAG, "Word '$word' was already swiped/tried recently. Skipping.")
                continue
            }
            val path = if (mode == SolveMode.STRAIGHT) {
                findStraightWord(word, grid)
            } else {
                findSnakeWord(word, grid)
            }

            if (path != null) {
                solvedPaths.add(path)
                triedSwipesCache[word] = currentTime
                if (cbBruteForce.isChecked) {
                    bruteForceTriedWords.add(word)
                }
                if (!masterDictionarySet.contains(word.uppercase())) {
                    mainHandler.post {
                        showNewWordPrompt(word.uppercase())
                    }
                }
                Log.d(TAG, "Word '$word' found in Grid. Path: ${path.map { "${it.char}(${it.x},${it.y})" }}")
            } else {
                Log.d(TAG, "Word '$word' NOT found in Grid.")
            }
        }

        if (solvedPaths.isEmpty()) {
            updateStatus("Solved 0/${words.size} grid words.")
            isSolving = false
            enableSolveButtons(true)
            if (cbAutoMode.isChecked || cbBruteForce.isChecked) {
                updateStatus("Auto/Brute loop: 0 solved, next scan in 1.5s...", true)
                mainHandler.postDelayed({
                    if (!isExpandedShown) {
                        startSolvingFlow()
                    }
                }, 1500)
            }
            return
        }

        updateStatus("Found ${solvedPaths.size}/${words.size} words. Swiping...", true)

        // Convert List<Letter> to List<Pair<x, y>>. For STRAIGHT mode, swipe directly from first to last letter.
        val swipeCoordinates = solvedPaths.map { path ->
            if (mode == SolveMode.STRAIGHT) {
                listOf(Pair(path.first().x, path.first().y), Pair(path.last().x, path.last().y))
            } else {
                path.map { letter -> Pair(letter.x, letter.y) }
            }
        }

        executeSequentialSwipes(swipeCoordinates)
    }

    private fun getMatchType(gridChar: Char, wordChar: Char): Int {
        val gc = gridChar.uppercaseChar()
        val wc = wordChar.uppercaseChar()
        if (gc == wc) return 0
        
        val isFuzzy = when (wc) {
            'A' -> gc in listOf('4', 'H', 'R', 'V', '@')
            'B' -> gc in listOf('8', '6', '3', 'P', 'R', 'D')
            'C' -> gc in listOf('G', 'O', '0', 'Q', '(')
            'D' -> gc in listOf('O', '0', 'Q', 'B', 'P', 'R')
            'E' -> gc in listOf('F', '3', '€', '£', 'C')
            'F' -> gc in listOf('E', 'P', 'T', '7')
            'G' -> gc in listOf('6', '9', 'C', 'Q', 'O', '0')
            'H' -> gc in listOf('N', 'M', 'I', '|', '1', 'L', 'K')
            'I' -> gc in listOf('1', '|', 'L', 'J', 'T', 'l', '[', ']', '(', ')', '!', '/', '\\', '.', ',', ':', ';', '-', 'H')
            'J' -> gc in listOf('I', 'l', '1', '|', 'T', 'L')
            'K' -> gc in listOf('X', 'H', 'R')
            'L' -> gc in listOf('I', '1', '|', 'l', 'J', 'T')
            'M' -> gc in listOf('N', 'W', 'H', 'V')
            'N' -> gc in listOf('M', 'H', 'Z', 'U', 'V', 'I', '|', '1')
            'O' -> gc in listOf('0', 'Q', 'D', 'C', 'G', 'U', '@', '°', 'º')
            'P' -> gc in listOf('R', 'F', 'B', 'D')
            'Q' -> gc in listOf('O', '0', 'D', 'G', 'C', '@', '9')
            'R' -> gc in listOf('P', 'B', 'K', 'A')
            'S' -> gc in listOf('5', '8', '2', 'Z', '$')
            'T' -> gc in listOf('I', '7', '1', '!', '|', '.', 'J', 'Y')
            'U' -> gc in listOf('V', 'O', '0', 'Y', 'H', 'N', 'L')
            'V' -> gc in listOf('U', 'Y', 'W', 'N')
            'W' -> gc in listOf('M', 'V', 'U', 'N')
            'X' -> gc in listOf('K', 'Y', '+')
            'Y' -> gc in listOf('V', 'U', 'T', 'J')
            'Z' -> gc in listOf('2', '7', 'S', 'N')
            else -> false
        }
        return if (isFuzzy) 1 else -1
    }

    private fun charMatches(gridChar: Char, wordChar: Char): Boolean {
        if (gridChar == '?') return true
        if (wordChar == '?') return true
        return getMatchType(gridChar, wordChar) >= 0
    }

    private fun findCircleWord(word: String, letters: List<Letter>): List<Letter>? {
        val path = mutableListOf<Letter>()
        val used = BooleanArray(letters.size)

        for (char in word) {
            var found = false
            for (i in letters.indices) {
                if (!used[i] && charMatches(letters[i].char, char)) {
                    path.add(letters[i])
                    used[i] = true
                    found = true
                    break
                }
            }
            if (!found) return null
        }
        return path
    }

    private fun findStraightWord(word: String, grid: List<List<Letter>>): List<Letter>? {
        val numRows = grid.size
        
        // Dynamically build direction options based on user selections
        val directions = mutableListOf<Pair<Int, Int>>()
        val allowHorizontal = cbDirHorizontal.isChecked
        val allowVertical = cbDirVertical.isChecked
        val allowDiagonal = cbDirDiagonal.isChecked

        if (allowHorizontal) {
            directions.add(Pair(0, 1))   // Right
            directions.add(Pair(0, -1))  // Left
        }
        if (allowVertical) {
            directions.add(Pair(1, 0))   // Down
            directions.add(Pair(-1, 0))  // Up
        }
        if (allowDiagonal) {
            directions.add(Pair(1, 1))   // Down-Right
            directions.add(Pair(1, -1))  // Down-Left
            directions.add(Pair(-1, -1)) // Up-Left
            directions.add(Pair(-1, 1))  // Up-Right
        }

        if (directions.isEmpty()) return null

        val maxMismatches = when {
            word.length <= 3 -> 0
            word.length <= 5 -> 1
            word.length <= 8 -> 2
            else -> 3
        }

        val minMatches = when {
            word.length <= 3 -> 1
            word.length <= 5 -> 2
            else -> 3
        }

        var bestPath: List<Letter>? = null
        var minScore = Int.MAX_VALUE

        for (r in 0 until numRows) {
            val numCols = grid[r].size
            for (c in 0 until numCols) {
                for ((dr, dc) in directions) {
                    val path = mutableListOf<Letter>()
                    var exactMatches = 0
                    var fuzzyMatches = 0
                    var mismatches = 0
                    var wildcards = 0
                    var match = true
                    for (i in word.indices) {
                        val nr = r + dr * i
                        if (nr !in 0 until numRows) {
                            match = false
                            break
                        }
                        val nc = c + dc * i
                        if (nc !in 0 until grid[nr].size) {
                            match = false
                            break
                        }
                        
                        val gridLetter = grid[nr][nc]
                        if (word[i] == '?') {
                            exactMatches++
                            path.add(gridLetter)
                        } else {
                            val matchType = getMatchType(gridLetter.char, word[i])
                            if (matchType == 0) {
                                exactMatches++
                                path.add(gridLetter)
                            } else if (matchType == 1) {
                                if (isFuzzyLookalikeAllowed(gridLetter.char, word[i], word.length)) {
                                    fuzzyMatches++
                                    path.add(gridLetter)
                                } else {
                                    mismatches++
                                    if (mismatches > maxMismatches) {
                                        match = false
                                        break
                                    }
                                    path.add(gridLetter)
                                }
                            } else if (isGridPlaceholder(gridLetter.char)) {
                                wildcards++
                                path.add(gridLetter)
                            } else {
                                mismatches++
                                if (mismatches > maxMismatches) {
                                    match = false
                                    break
                                }
                                path.add(gridLetter)
                            }
                        }
                    }
                    if (match && (exactMatches + fuzzyMatches >= minMatches)) {
                        val score = exactMatches * 0 + fuzzyMatches * 1 + wildcards * 2 + mismatches * 4
                        if (score < minScore) {
                            minScore = score
                            bestPath = path
                        }
                    }
                }
            }
        }
        return bestPath
    }

    private fun findSnakeWord(word: String, grid: List<List<Letter>>): List<Letter>? {
        val numRows = grid.size
        var bestPath: List<Letter>? = null
        var minScore = Int.MAX_VALUE

        val maxMismatches = when {
            word.length <= 4 -> 0
            word.length <= 6 -> 1
            word.length <= 9 -> 2
            else -> 3
        }

        val minMatches = when {
            word.length <= 3 -> 1
            word.length <= 5 -> 2
            else -> 3
        }

        val visited = Array(numRows) { r -> BooleanArray(grid[r].size) }

        fun dfs(r: Int, c: Int, wordIndex: Int, exactMatches: Int, fuzzyMatches: Int, mismatches: Int, wildcards: Int, path: MutableList<Letter>) {
            if (wordIndex == word.length) {
                if (exactMatches + fuzzyMatches >= minMatches) {
                    val score = exactMatches * 0 + fuzzyMatches * 1 + wildcards * 2 + mismatches * 4
                    if (score < minScore) {
                        minScore = score
                        bestPath = path.toList()
                    }
                }
                return
            }
            if (r !in 0 until numRows || c !in 0 until grid[r].size || visited[r][c]) return

            val gridLetter = grid[r][c]
            
            var newExact = exactMatches
            var newFuzzy = fuzzyMatches
            var newMismatches = mismatches
            var newWildcards = wildcards

            if (word[wordIndex] == '?') {
                newExact++
            } else {
                val matchType = getMatchType(gridLetter.char, word[wordIndex])
                if (matchType == 0) {
                    newExact++
                } else if (matchType == 1) {
                    if (isFuzzyLookalikeAllowed(gridLetter.char, word[wordIndex], word.length)) {
                        newFuzzy++
                    } else {
                        newMismatches++
                    }
                } else if (isGridPlaceholder(gridLetter.char)) {
                    newWildcards++
                } else {
                    newMismatches++
                }
            }

            if (newMismatches > maxMismatches) return

            visited[r][c] = true
            path.add(gridLetter)

            for (dr in -1..1) {
                for (dc in -1..1) {
                    if (dr == 0 && dc == 0) continue
                    dfs(r + dr, c + dc, wordIndex + 1, newExact, newFuzzy, newMismatches, newWildcards, path)
                }
            }

            path.removeAt(path.size - 1)
            visited[r][c] = false
        }

        for (r in 0 until numRows) {
            for (c in 0 until grid[r].size) {
                dfs(r, c, 0, 0, 0, 0, 0, mutableListOf())
            }
        }

        return bestPath
    }

    private fun executeSequentialSwipes(paths: List<List<Pair<Float, Float>>>) {
        val accessibilityService = WordSolverAccessibilityService.instance
        if (accessibilityService == null) {
            updateStatus("Error: Accessibility inactive.")
            isSolving = false
            enableSolveButtons(true)
            return
        }

        // 1. Retrieve user-configured speed duration
        val swipeDuration = when (rgSwipeSpeed.checkedRadioButtonId) {
            R.id.rb_speed_fast -> 200L
            R.id.rb_speed_medium -> 450L
            R.id.rb_speed_slow -> 850L
            else -> 450L
        }

        // 2. Retrieve user-configured delay between words
        val actionDelay = when (rgActionDelay.checkedRadioButtonId) {
            R.id.rb_delay_short -> 350L
            R.id.rb_delay_medium -> 800L
            R.id.rb_delay_long -> 1500L
            else -> 800L
        }

        var currentIndex = 0

        fun swipeNext() {
            if (!isSolving || isExpandedShown || isDestroyedService) {
                Log.d(TAG, "Swiping cancelled/paused.")
                isSolving = false
                enableSolveButtons(true)
                return
            }

            if (currentIndex >= paths.size) {
                updateStatus("Finished solving! Swiped ${paths.size} words.")
                isSolving = false
                enableSolveButtons(true)
                if (cbAutoMode.isChecked || cbBruteForce.isChecked) {
                    updateStatus("Auto/Brute loop: Next scan in 1.5s...", true)
                    mainHandler.postDelayed({
                        if (!isExpandedShown && isSolving && !isDestroyedService) {
                            startSolvingFlow()
                        }
                    }, 1500)
                }
                return
            }

            val currentPath = paths[currentIndex]
            updateStatus("Swiping word ${currentIndex + 1}/${paths.size}...", true)
            Log.d(TAG, "Initiating Accessibility Swipe Gesture ${currentIndex + 1}/${paths.size} | Points path: $currentPath")

            accessibilityService.performSwipeGesture(currentPath, swipeDuration) { success ->
                Log.d(TAG, "Gesture ${currentIndex + 1}/${paths.size} complete status: success=$success")
                currentIndex++
                mainHandler.postDelayed({
                    swipeNext()
                }, actionDelay)
            }
        }

        swipeNext()
    }

    override fun onDestroy() {
        hideNewWordPrompt()
        isDestroyedService = true
        isSolving = false
        mainHandler.removeCallbacksAndMessages(null)
        super.onDestroy()
        cleanupMediaProjection()
        removeLettersOverlay()
        try {
            if (isExpandedShown) windowManager.removeView(expandedView)
            windowManager.removeView(minimizedView)
        } catch (e: Exception) {
            e.printStackTrace()
        }
        mediaProjection?.stop()
    }
}

