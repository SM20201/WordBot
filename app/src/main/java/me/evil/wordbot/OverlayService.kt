    private fun captureScreen(callback: (Bitmap?) -> Unit) {
        val reader = imageReader
        if (reader == null) {
            callback(null)
            return
        }

        // 1. Temporarily hide our overlay graphics to get a clean screenshot
        val menuShown = isExpandedShown
        if (menuShown) {
            expandedView.visibility = View.GONE
        }
        minimizedView.visibility = View.GONE
        removeLettersOverlay()

        // 2. Clear any old images from reader queue so it has space for a new one
        while (true) {
            val img = reader.acquireLatestImage() ?: break
            img.close()
        }

        // 3. Wait for compositor to render a new frame without overlays, then extract directly
        mainHandler.postDelayed({
            try {
                val image = reader.acquireLatestImage()
                if (image != null) {
                    val planes = image.planes
                    val buffer = planes[0].buffer
                    val pixelStride = planes[0].pixelStride
                    val rowStride = planes[0].rowStride
                    val rowPadding = rowStride - pixelStride * reader.width

                    val rawBitmap = Bitmap.createBitmap(
                        reader.width + rowPadding / pixelStride,
                        reader.height,
                        Bitmap.Config.ARGB_8888
                    )
                    rawBitmap.copyPixelsFromBuffer(buffer)

                    // Crop screen padding
                    val croppedBitmap = Bitmap.createBitmap(rawBitmap, 0, 0, reader.width, reader.height)
                    rawBitmap.recycle()

                    image.close()
                    callback(croppedBitmap)
                } else {
                    callback(null)
                }
            } catch (e: Exception) {
                e.printStackTrace()
                callback(null)
            } finally {
                // ALWAYS restore overlay visibility
                if (menuShown) {
                    expandedView.visibility = View.VISIBLE
                }
                minimizedView.visibility = View.VISIBLE
            }
        }, 220)
    }

