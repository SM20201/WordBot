    private fun initMediaProjection() {
        val manager = getSystemService(Context.MEDIA_PROJECTION_SERVICE) as MediaProjectionManager
        val resultCode = MainActivity.captureResultCode
        val data = MainActivity.captureIntentData
        if (resultCode != 0 && data != null) {
            val proj = manager.getMediaProjection(resultCode, data)
            if (proj != null) {
                mediaProjection = proj
                proj.registerCallback(object : MediaProjection.Callback() {
                    override fun onStop() {
                        super.onStop()
                        mediaProjection = null
                        cleanupMediaProjection()
                    }
                }, mainHandler)

                // Setup the persistent capture display immediately
                setupPersistentVirtualDisplay()
            } else {
                Toast.makeText(this, "Error: Media Projection initialization failed!", Toast.LENGTH_LONG).show()
                stopSelf()
            }
        } else {
            Toast.makeText(this, "Error: Media Projection not authorized!", Toast.LENGTH_LONG).show()
            stopSelf()
        }
    }

    private fun setupPersistentVirtualDisplay() {
        val proj = mediaProjection ?: return
        
        val metrics = DisplayMetrics()
        windowManager.defaultDisplay.getRealMetrics(metrics)
        val width = metrics.widthPixels
        val height = metrics.heightPixels
        val density = metrics.densityDpi

        val reader = ImageReader.newInstance(width, height, PixelFormat.RGBA_8888, 2)
        imageReader = reader

        virtualDisplay = proj.createVirtualDisplay(
            "WordBotCapture",
            width,
            height,
            density,
            DisplayManager.VIRTUAL_DISPLAY_FLAG_AUTO_MIRROR,
            reader.surface,
            null,
            null
        )
    }

    private fun cleanupMediaProjection() {
        try {
            virtualDisplay?.release()
            virtualDisplay = null
            imageReader?.close()
            imageReader = null
        } catch (e: Exception) {
            e.printStackTrace()
        }
    }

